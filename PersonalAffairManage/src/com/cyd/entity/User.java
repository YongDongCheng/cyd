package com.cyd.entity;

public class User {
	private String identicard_id;//身份证
	private String name;//姓名
	private int age;//年龄
	private String sex;//性别
	private int salary;//收入
	private String marry_condition;//婚姻状况
	private String job;//职业
	private String address;//地址
	private String phone;//手机号码
	public String getIdenticard_id() {
		return identicard_id;
	}
	public void setIdenticard_id(String identicard_id) {
		this.identicard_id = identicard_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public String getMarry_condition() {
		return marry_condition;
	}
	public void setMarry_condition(String marry_condition) {
		this.marry_condition = marry_condition;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	

}
